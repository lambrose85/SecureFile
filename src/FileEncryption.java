	public static byte[] encrypt(byte[] input, byte[] k) {
		
		byte[] out = new byte[16];
		byte[][] state = to_state(input);
		int[] keys = keyExpansion(k);
		
		addRoundKey(state, to_state(get_curr_key(keys, 0)));
		
		for(int i=1;i<10;i++) {
			subBytes(state);
			shiftRows(state);
			mixColumn(state);
			addRoundKey(state, to_state(get_curr_key(keys, i)));
		}
		subBytes(state);
		shiftRows(state);
		addRoundKey(state, to_state(get_curr_key(keys, 10)));
		
		int index = 0;
		for(int i=0;i<4;i++) {
			for(int j=0;j<4;j++) {
				out[index] = state[j][i];
				index++;
			}
		}
		
		return out;
	}
	
	private static void subBytes(byte[][] state) {

		for(int i=0;i<4;i++) {
			for(int j=0;j<4;j++) {
				state[i][j] = (byte) sbox[(state[i][j] >> 4) & 0x0F][state[i][j] & 0x0F];
			}
		}

	}
	
	private static void invSubBytes(byte[][] state) {
		
		for(int i=0;i<4;i++) {
			for(int j=0;j<4;j++) {
				state[i][j] = (byte) invSbox[(state[i][j] >> 4) & 0x0F][state[i][j] & 0x0F];
			}
		}
		
	}
	
	private static void shiftRows(byte[][] state) {

		for(int i=0;i<4;i++) {
			rotate_left(state[i], i);
		}

	}

	private static void rotate_left(byte[] input, int n) {
		
		for(int j=0;j<n;j++) {
			byte front = input[0]; 
			
			for(int i=0;i<input.length-1;i++) {
				input[i] = input[i+1];
			}
			
			input[input.length-1] = front;
		}
		
	}
	
	private static void invShiftRows(byte[][] state) {

		for(int i=0;i<4;i++) {
			rotate_right(state[i], i);
		}

	}
	
	
	private static void rotate_right(byte[] input, int n) {
		
		for(int j=0;j<n;j++) {
			byte back = input[input.length-1]; 
			
			for(int i=input.length-1;i>0;i--) {
				input[i] = input[i-1];
			}
			
			input[0] = back;
		}
		
	}

